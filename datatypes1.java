public class DetailsWithDataTypes {
    public static void main(String[] args) {
        // Primitive Data Types
        byte byteValue = 100;
        short shortValue = 30000;
        int intValue = 100000;
        long longValue = 10000000000L;
        float floatValue = 3.14f;
        double doubleValue = 3.141592653589793;
        char charValue = 'A';
        boolean booleanValue = true;

        // Personal Details
        String name = "Y. Mallikarjuna Rao";
        int age = 22;
        String district = "Guntur";
        String mandal = "Chebrolu";
        String address = "9-10, Near Cinema Hall Bazar, Chebrolu";
        String email = "malliyannam67@gmail.com";
        long phoneNo = 9391319653L;
        String collegeName = "Malineni Perumallu Educational Society's Group of Institutions, Guntur";

        // Displaying Data Types
        System.out.println("Primitive Data Types:");
        System.out.println("byteValue: " + byteValue);
        System.out.println("shortValue: " + shortValue);
        System.out.println("intValue: " + intValue);
        System.out.println("longValue: " + longValue);
        System.out.println("floatValue: " + floatValue);
        System.out.println("doubleValue: " + doubleValue);
        System.out.println("charValue: " + charValue);
        System.out.println("booleanValue: " + booleanValue);

        // Displaying Personal Details
        System.out.println("\nPersonal Details:");
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("District: " + district);
        System.out.println("Mandal: " + mandal);
        System.out.println("Address: " + address);
        System.out.println("Email: " + email);
        System.out.println("Phone No: " + phoneNo);
        System.out.println("College Name: " + collegeName);
    }
}
